#include "3-calc.h"

/**
 * op_add - fun sum two number
 * @a: one number
 * @b: two number
 * Return: integer sum
*/
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - fun op_sub two number
 * @a: one number
 * @b: two number
 * Return: integer sub
*/
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - fun op_mul two number
 * @a: one number
 * @b: two number
 * Return: integer op_mul
*/
int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - fun op_div two number
 * @a: one number
 * @b: two number
 * Return: integer op_div
*/
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - fun op_mod two number
 * @a: one number
 * @b: two number
 * Return: integer op_mod
*/
int op_mod(int a, int b)
{
return (a % b);
}
